# Master function
def clean_data(df, add_sos:bool): 
    """
    Input dataframe, get back data frame with clean labels, game ids, 
    and game location dummy variables 

    :param - df 
    :return - clean df
    """

    # Remove NAs 
    df = df.dropna()

    # Match labels 
    df = match_labels(df)

    # Add srs and sos 
    with open('season_srs_dict.json', 'r', encoding='UTF-8') as srs_dict:
        season_srs_dict = json.load(srs_dict)

    for key, value in season_srs_dict.items():
        df.loc[df['school'] == key, 'srs'] = value

    if add_sos:
        with open('season_sos_dict.json', 'r', encoding='UTF-8') as sos_dict:
            season_sos_dict = json.load(sos_dict)

        for key, value in season_sos_dict.items():
            df.loc[df['school'] == key, 'sos'] = value

    # Correct data types 
    df = correct_type(df)

    # Create game ids 
    df = create_game_id(df)

    #Create game location dummy variables 
    df = make_location_dumb(df)

    return df
