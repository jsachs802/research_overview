### Create game_id 
def create_game_id(df): 
    """
    Input df and get back df with game id 

    Dataframe includes duplicate games with columns switched between 'school' and 'opp_team_id' 

    :param - df 
    :return - df with game_id
    """
    ## Create game_id 
    # Best way could be to make a dictionary, where each team is given a number 
    # create a list that includes both 'school' and 'opp_team_id' to include opponents that we don't keep track of

    # create unique team list
    unique_teams = pd.concat([pd.Series(df['school'].unique()), 
                              pd.Series(df['opp_team_id'].unique())])  # create list of unique labels in 'school' column
    unique_teams = unique_teams.unique() # get unique list

    # Create team/id dictionary of key/value pairs 
    team_keys = {}
    i = 1
    for team in unique_teams:
        team_keys[team] = str(i)
        i += 1

    # Create date/id dictionary of key/value pairs  
    date_keys = {}
    i = 1
    for day in df['date'].unique():
        date_keys[day] = str(i)
        i += 1

    matchup_id = [] # create list to hold game_ids

    # loop through 'school', 'opp_team_id' and 'date' column of df
    for a, b, c in zip(df['school'], df['opp_team_id'], df['date']):
        if a < b: # keep alphabetical order of teams
            matchup_id.append(team_keys[a] + team_keys[b] + date_keys[c]) # append strings as id
        else:
            matchup_id.append(team_keys[b] + team_keys[a] + date_keys[c])

    # Create new column in df
    df['game_id'] = matchup_id

    return df


# Create game location dummy variables 
def make_location_dumb(df):
    """
    Input data frame, return dataframe with game_location as two dummies: 
        home, away, with neutral being 0's in both columns

    :param - df 
    :return - df with dummies for game location 
    """

    # Create dummies for game_location
    df = pd.get_dummies(df, columns=['game_location'], drop_first=False)

    # Rename columns
    df = df.rename(columns={'game_location_@': 'away', 
                     'game_location_N': 'neutral',
                     'game_location_h': 'home'})
    
    df = df.drop('neutral', axis=1)

    # return df with dummy columns 'home' and 'away'
    return df



